package controle;

public class SwitchSemBreak {
	public static void main(String[] args) {

		// switch sem break executa tudo abaixo.
		// a ordem vai de cima pra baixo
		
		
		String faixa = "preta";

		switch (faixa.toLowerCase()) {
		case "preta":
			System.out.println("Sei de tudo");
		case "marrom":
			System.out.println("Sei mais ou menos");
		case "roxa":
		System.out.println("Sei o Hean Godan");
		
		
		}

	}
}
